package com.xp.javadesign.brige;

public class BaseCoffee {
    /**
     * 杯型：小，中，大，超大
     */
    private String cupSize;
    /**
     * 咖啡豆：普通，精选
     */
    private String coffeeBean;
    /**
     * 温度：加冰，正常冰，少冰
     */
    private String temperature;
    /**
     * 名称
     */
    private String name;
    /**
     * 甜度：不另外加糖，少少甜，少甜，标准甜，微甜，五分，七分，三分等等。。。。
     */
    private String sugar;
    /**
     * 奶：不加，单倍，双倍
     */
    private String milk;
    /**
     * 咖啡浓度：默认，加份浓缩
     */
    private String Strength;
}
